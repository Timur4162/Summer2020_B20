package day18_For_Loop;

public class for_Loop {

    public static void main(String[] args) {

        System.out.println("Hello World");
        System.out.println("Hello World");
        System.out.println("Hello World");
        System.out.println("===============================");

        for (int i = 0; i < 14; i++) {
            System.out.println("Hello World");
        }


    }

}
