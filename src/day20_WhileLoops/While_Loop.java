package day20_WhileLoops;

public class While_Loop {
    public static void main(String[] args) {

        if (true){
            System.out.println("Hello World");      // prints only ONE TIME
        }

        System.out.println("=======================");

        while (true){     // must be TRUE to be printed INFINITE Time ====> repeated IF STATEMENT
            System.out.println("Hello World");  // will be printed INFINITE TIME
        }











    }
}
