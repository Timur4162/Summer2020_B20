package day3_sequences_Variables;

public class EscapeSequences {
    public static void main(String[] args) {
        System.out.println("Hello World \n\tMy Name Is Cybertek");
        System.out.println("\t\t I'm in Colorado");
        System.out.println("\n\n\nI love programming");

        System.out.println("\\"); // this prints me one backs lash
        System.out.println("/"); // this prints me one front slash

        System.out.println("\\\\");

        System.out.println("My name is \'Timur\'");
        System.out.println("My favorite TV-series: \"Game of Thrones\"");
    }
}
